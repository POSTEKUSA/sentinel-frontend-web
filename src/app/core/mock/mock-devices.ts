import { DeviceSummary } from '../models/device-summary.model';
import { TelemetryEvent } from '../models/telemetry-event.model';

function minutesAgo(minutes: number): string {
  return new Date(Date.now() - minutes * 60 * 1000).toISOString();
}

function makeEvents(deviceId: string): TelemetryEvent[] {
  return Array.from({ length: 8 }, (_, i) => {
    const index = i % 4;
    const eventType: TelemetryEvent['eventType'] =
      index === 0 ? 'heartbeat' :
      index === 1 ? 'location' :
      index === 2 ? 'health' : 'sync';

    return {
      id: `${deviceId}-evt-${i}`,
      deviceId,
      eventType,
      timestamp: minutesAgo(i * 3),
      payload: {
        sequence: i,
        source: 'android-agent',
        extra: `data-${i}`,
      },
    };
  });
}

